// 질문을 표시하는 함수
function showQuestion() {
    const questionContainer = document.getElementById('question-container');
    questionContainer.innerHTML = ''; // 이전 질문을 초기화

    const questionData = questions[currentQuestionIndex];
    const questionText = document.createElement('h2');
    questionText.textContent = questionData.question;

    questionContainer.appendChild(questionText);

    questionData.options.forEach(option => {
        const button = document.createElement('button');
        button.textContent = option;
        button.onclick = () => handleAnswer(option);
        questionContainer.appendChild(button);
    });
}

// 사용자가 답변을 선택했을 때 처리하는 함수
function handleAnswer(option) {
    const currentQuestion = questions[currentQuestionIndex];
    userAnswers[currentQuestion.key] = option;

    currentQuestionIndex++;

    // 마지막 질문이면 결과를 보여줌
    if (currentQuestionIndex === questions.length) {
        showResult();
    } else {
        showQuestion();
    }
}

// 결과를 보여주는 함수
function showResult() {
    const resultDiv = document.getElementById('result');
    const saddleRecommendation = document.getElementById('saddle-recommendation');

    // 선택한 답변을 바탕으로 추천 안장 찾기
    const matchingSaddles = saddleData.filter(saddle => {
        return Object.keys(userAnswers).every(key => {
            // 각 선택 항목이 여러 값 중 하나라도 포함되는지 확인
            if (Array.isArray(saddle[key])) {
                return saddle[key].includes(userAnswers[key]);
            }
            return saddle[key] === userAnswers[key];
        });
    });

    if (matchingSaddles.length > 0) {
        const saddleNames = matchingSaddles.map(saddle => saddle.name).join(', ');
        saddleRecommendation.textContent = `추천하는 안장: ${saddleNames}`;
    } else {
        saddleRecommendation.textContent = '해당되는 안장이 없습니다.';
    }

    resultDiv.style.display = 'block';
}

// 다시 시작하는 함수
